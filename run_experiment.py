"""
run_experiment.py
Main entry point for all final experiments.

You choose the version with two arguments:
- --mode biomedical or methodological
- --strategy global or mondrian

Examples:
python run_experiment.py --dataset 2023_1000k --mode biomedical --strategy global
python run_experiment.py --csv data/natality2022_clean_smoking_birthweight_1000k.csv --dataset 2022_1000k --mode methodological --strategy mondrian
"""

from __future__ import annotations

import argparse
import pickle
from pathlib import Path

import pandas as pd

from conformal_core import (
    ALPHA,
    CQR_Q_HI,
    CQR_Q_LO,
    DEVICE,
    DEFAULT_CLEAN_CSV,
    HYPERPARAMETER_GRID,
    RANDOM_STATE,
    ExperimentConfig,
    get_or_create_split,
    load_dataset,
    prepare_data,
)
from conformal_methods import run_three_methods


def parse_args():
    parser = argparse.ArgumentParser()
    parser.add_argument("--csv", default=DEFAULT_CLEAN_CSV, help="Path to the cleaned CSV file generated by prepare_cdc_natality.py.")
    parser.add_argument("--dataset", required=True, help="Short dataset name used in output files, e.g. 2023_1000k.")
    parser.add_argument("--mode", choices=["biomedical", "methodological"], required=True)
    parser.add_argument("--strategy", choices=["global", "mondrian"], required=True)
    parser.add_argument("--out", default=None, help="Output folder. If omitted, it is created automatically.")
    parser.add_argument("--epochs", type=int, default=250)
    parser.add_argument("--patience", type=int, default=25)
    parser.add_argument("--batch-size", type=int, default=512)
    parser.add_argument("--no-grid", action="store_true", help="Disable the small hyperparameter grid and use default HP only.")
    return parser.parse_args()


def save_method_outputs(result: dict, out_dir: Path, cfg: ExperimentConfig) -> None:
    """Save pkl and CSV files for one method."""
    tag = f"{result['method_key']}_{cfg.model_mode}_{cfg.strategy}_{cfg.dataset_name}"

    with open(out_dir / f"outputs_{tag}.pkl", "wb") as f:
        pickle.dump(result, f)

    result["summary_all"].to_csv(out_dir / f"summary_{tag}.csv", index=False)
    result["group_metrics_all"].to_csv(out_dir / f"group_metrics_{tag}.csv", index=False)
    result["q_table_all"].to_csv(out_dir / f"q_table_{tag}.csv", index=False)


def main():
    args = parse_args()
    out_dir = args.out or f"results_{args.mode}_{args.strategy}_{args.dataset}"

    cfg = ExperimentConfig(
        csv_file=args.csv,
        dataset_name=args.dataset,
        model_mode=args.mode,
        strategy=args.strategy,
        out_dir=out_dir,
        epochs=args.epochs,
        patience=args.patience,
        batch_size=args.batch_size,
        use_grid=not args.no_grid,
    )

    out = Path(cfg.out_dir)
    out.mkdir(parents=True, exist_ok=True)

    print("=" * 80)
    print(f"Final conformal experiment | mode={cfg.model_mode} | strategy={cfg.strategy}")
    print(f"CSV: {cfg.csv_file}")
    print(f"Output folder: {cfg.out_dir}")
    print(f"Device: {DEVICE} | seed: {RANDOM_STATE}")
    print(f"Conformal alpha={ALPHA} => target coverage={(1 - ALPHA) * 100:.0f}%")
    print(f"CQR direct quantiles: q{int(CQR_Q_LO * 100):02d} and q{int(CQR_Q_HI * 100):02d}")
    print(f"Group column: {cfg.group_col}")
    print(f"Grid search: {'ON' if cfg.use_grid else 'OFF'} | candidates={len(HYPERPARAMETER_GRID) if cfg.use_grid else 1}")
    print("=" * 80)

    df = load_dataset(cfg.csv_file)
    split = get_or_create_split(df, cfg)
    data = prepare_data(df, cfg, split)

    print(f"Features used ({len(data['features'])}):")
    for f in data["features"]:
        print(f"  - {f}")

    results = run_three_methods(data, cfg)

    summaries, groups, qtables = [], [], []
    for result in results.values():
        save_method_outputs(result, out, cfg)
        summaries.append(result["summary_all"])
        groups.append(result["group_metrics_all"])
        qtables.append(result["q_table_all"])

    comparison_summary = pd.concat(summaries, ignore_index=True)
    comparison_groups = pd.concat(groups, ignore_index=True)
    comparison_q = pd.concat(qtables, ignore_index=True)

    comparison_summary.to_csv(out / f"comparison_summary_{cfg.model_mode}_{cfg.strategy}_{cfg.dataset_name}.csv", index=False)
    comparison_groups.to_csv(out / f"comparison_groups_{cfg.model_mode}_{cfg.strategy}_{cfg.dataset_name}.csv", index=False)
    comparison_q.to_csv(out / f"comparison_q_{cfg.model_mode}_{cfg.strategy}_{cfg.dataset_name}.csv", index=False)

    with open(out / f"comparison_all_{cfg.model_mode}_{cfg.strategy}_{cfg.dataset_name}.pkl", "wb") as f:
        pickle.dump({"config": cfg, "results": results, "summary": comparison_summary, "groups": comparison_groups}, f)

    print("\nMain outputs saved in:", cfg.out_dir)
    print("- comparison_summary_*.csv")
    print("- comparison_groups_*.csv")
    print("- comparison_q_*.csv")
    print("- outputs_<method>_*.pkl")

    print("\nFinal summary:")
    show_cols = ["method_key", "strategy", "coverage_%", "avg_width_kg", "MAE_kg", "RMSE_kg", "Q_kg", "train_s"]
    print(comparison_summary[show_cols].to_string(index=False))

    print("\nSelected hyperparameters:")
    for key, res in results.items():
        ms = res.get("model_selection", {})
        print(f"- {key}: {ms.get('selected_hp')}")


if __name__ == "__main__":
    main()
